/// A utility class that provides access to the package's assets.
///
/// This class contains constant references to all assets used in the package,
/// including fonts and other resources.
class Assets {
  /// Private constructor to prevent instantiation
  Assets._();

  /// Path to the regular weight Roboto font file
  static const String fontRobotoRegular = 'assets/fonts/Roboto-Regular.ttf';

  /// Path to the bold weight Roboto font file
  static const String fontRobotoBold = 'assets/fonts/Roboto-Bold.ttf';
}
